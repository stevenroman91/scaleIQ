const PITCH_GUIDE_SHEET = `# Le Guide Ultime pour Le Pitch Parfait

Si les prospects vous ignorent ou vous rejettent, il est probable que votre pitch manque de precision.

## Mindset

Les êtres humains ont tous un sujet préféré : eux-mêmes.

Plus votre discours parle de vous, plus ils seront susceptibles de se désintéresser.

## Des caractéristiques et avantages aux problèmes

Les fonctionnalités concernent vous, donc ce n'est pas le meilleur point de départ. Les avantages concernent le prospect, donc c'est mieux. **MAIS** : Nos cerveaux sont câblés pour être deux fois plus réactifs à s'éloigner qu'à se diriger vers le plaisir. Alors, commencez par les problèmes.

**Conseil clé :** Identifiez d'abord les douleurs et frustrations de vos prospects avant de présenter votre solution.

## Définir les problèmes et les douleurs

**Quels sont les problèmes que vous/votre entreprise résolvez ?**

Plus le problème est grand et spécifique, mieux c'est. Plus il est spécifique à votre profil de client idéal (PCI), plus il résonnera.

### Exemples

- **Trop vague** - Je suis invité par des startups pour résoudre des problèmes liés aux ventes.
- **Spécifique** - Je suis invité par des fondateurs de SaaS pour résoudre des problèmes concernant la création d'une fonction de vente sortante.
- **Très spécifique** - Je suis invité par des fondateurs de SaaS B2B en phase pré-série A qui n'ont aucune idée de comment construire une fonction de vente sortante et...

**Important :** N'utilisez jamais plus de 3 problèmes. À moins que vous ne vendiez des pizzas, votre produit ne rendra pas tout le monde heureux. Trop large = pas crédible.

## Langue

**Plus vous utilisez le langage exact de vos prospects, plus vous serez efficace.**

Par exemple, les agences de recrutement appellent généralement la prospection « développement commercial » (abrégé en DC) ; les commerciaux sont également appelés consultants, et non pas SDR.

### Par conséquent :

- **Moins efficace** - Je suis invité par des propriétaires d'agences de recrutement qui sont frustrés parce que leurs équipes ne prospectent pas assez.
- **Plus efficace** - Je suis invité par des propriétaires d'agences de recrutement qui sont frustrés parce que leurs consultants sont réticents à faire du développement commercial.

**Astuce :** Écoutez attentivement comment vos clients parlent de leurs problèmes et utilisez exactement les mêmes termes dans votre pitch.

## Impact & Conséquences

**Que signifient ces problèmes pour le prospect ? C'est là que votre connaissance de l'ICP fera la différence.**

Comment ces problèmes se manifestent-ils dans le monde du prospect ? Vous devez à la fois cartographier l'impact du problème et ses conséquences.

### Fondateur technologique pré-série A

- **Problème :** Un manque de nouvelles opportunités qualifiées
- **Impact :** Les investisseurs perdent confiance et réduisent les fonds
- **Conséquences :** L'entreprise pourrait faire faillite

### Fondateur technologique après la série A

- **Problème :** Un manque de nouvelles opportunités qualifiées
- **Impact :** Concentration sur les ventes au détriment d'autres domaines
- **Conséquences :** Croissance de l'entreprise ralentie, objectifs non atteints

**Conclusion :** Plus vous comprenez l'impact et les conséquences, plus vous êtes susceptible de viser juste et de qualifier correctement les prospects.

## Différents canaux, différents tons

**Où apparaîtra votre énoncé de problème ?**

### Courriel à froid / LinkedIn / Courrier direct

Concentrez-vous sur un problème à la fois.

### Slogan

Concentrez-vous sur le plus gros problème dans votre slogan et utilisez les tous les tons en même temps.

### Découverte/Démo

Concentrez-vous sur le fait de poser des questions et de découvrir ce qui est pertinent – ne parlez pas de problèmes que le prospect n'a pas.

### Appel à froid

Vous pouvez énoncer les trois si vous êtes pertinent et direct. Deux problèmes fonctionnent bien, un problème est bon s'il s'agit d'une plaie béante.

**Adaptabilité :** Chaque canal de communication nécessite une approche différente. Adaptez votre message selon le contexte et le niveau d'engagement du prospect.

---

La maîtrise du pitch parfait est un art qui se perfectionne avec la pratique. Concentrez-vous sur les problèmes réels de vos prospects, utilisez leur langage, et montrez clairement l'impact de ces problèmes. La précision et la pertinence sont vos meilleurs alliés.`;

